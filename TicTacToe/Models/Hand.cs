namespace TicTacToe.Models;

/// <summary>
/// Represents the possible values for a player's hand in the TicTacToe game.
/// </summary>
public enum Hand
{
    /// <summary>
    /// Represents the 'X' hand, typically used by one of the players.
    /// </summary>
    X,

    /// <summary>
    /// Represents the 'O' hand, typically used by the other player.
    /// </summary>
    O
}
